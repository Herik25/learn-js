const fruits = ["apple", "banana", "orange"];
console.log("Original array:", fruits); // Output: ["apple", "banana", "orange"]

const shiftedElement = fruits.shift();
console.log("Shifted element:", shiftedElement); // Output: "apple"
console.log("Array after shift:", fruits); // Output: ["banana", "orange"]



// The shift() method removes the first item of an array.
// The shift() method changes the original array.
// The shift() method returns the shifted element.
